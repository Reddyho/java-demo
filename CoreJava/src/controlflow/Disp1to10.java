public class Disp1to10 {

	public static void main(String[] args) {

		int a=1;
		while (a<=10){
			System.out.println(a);
			a+=1;

		}

		//reverse looping
		int b=20;
		while(b>=1) {
			System.out.print(b+" ");
			b-=1;

		}

	}

}
