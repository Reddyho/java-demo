public class ForDemo {
	public static void main(String[] args) {
		int i;
		for ( i = 1; i < 11; i++) {
			System.out.print(i+"\t");
		}
		System.out.println("\t");
		
		for (i = 10; i > 0; i--) {
			System.out.print(i+"\t");
			
		}
		for (i = 10; i > 0; i--) {
		System.out.println("hon");
		}
	}

}
